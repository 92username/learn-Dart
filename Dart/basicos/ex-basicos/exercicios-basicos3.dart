//Exercício 3: Comparação de Valores
// Crie duas variáveis a e b com valores diferentes e use operadores
// de comparação (==, !=, >, <, etc.) para exibir os resultados dessas
// comparações

void main() {
  var a = 10;
  var b = 5;

  print('a: $a');
  print('b: $b');
  print('');

  print('a == b: ${a == b}');
  print('a != b: ${a != b}');
  print('a > b: ${a > b}');
  print('a < b: ${a < b}');
  print('a >= b: ${a >= b}');
  print('a <= b: ${a <= b}');
}
